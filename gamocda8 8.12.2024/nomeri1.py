def func(list1):
    list2 = []
    #უნდა შევქმნათ ფორ ციკლი რომელიც გადაუვლის მთლიან სტრინგს და შეამოწმებს არის თუ არა იგი 0ზე მეტი ამის შემდეგ თუ მეტია 0ზე დაემატება ლისთ2-ს და საბოლოოდ დავაბრუნებ მათ ჯამს
    for i in list1:
        if i > 0:
            list2.append(i)
    return sum(list2)

print(func([1, -4, 7, 12]))
print(func([-1, -2, -3, -4]))
print(func([]))